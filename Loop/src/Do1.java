
public class Do1 {

	public static void main(String[] args) {
		
		int a = 1; //초기값
		do {
			System.out.print(a+" ");
			a++; //증감
		} while(a<11); // 범위값
		System.out.println();
		/*응용문제

		5~0까지 출력되는 do ~ while 문
		
		*/
		int b = 5;
		do {
			System.out.print(b+" ");
			b--;
		}while(b>=0);
		System.out.println();
		
		int c = 50;
		int d = 60;
		do {
			System.out.print(c+" ");
			c++;
		}while(c<=d);
		System.out.println();
		
		

	}

}
