
public class Method12 {

	public static void main(String[] args) {
		
		
		bbox2 bb2 = new bbox2();
		bb2.b2("이순신");
		bbox bb = new bbox();
		System.out.println(bb.user_info);
		System.out.println(bb2.abc());
	}

	
}

class bbox{ //기본 외부 class1 main에서 필요한 사항을 체크 
	protected String user_info;
	private String user_pw;
	public String user_rpw;
	public bbox(){
		this.user_info = "홍길동";
		this.user_pw ="abcd";
		this.user_rpw = this.user_pw.replace("abcd", "0x5d323dfd");
	}
	public void b() {
		System.out.println(user_pw);
	}
	public String abc() {
		String email = "hong@nate.com";
		return email;
	}
}
class bbox2 extends bbox{ //bbox 에 대한 모든 정보를 bbox2와 공유하게됨
	// 단 , private로 셋된 값은 공유하지 않음
	
	public void b2(String nm) { // main 클래스에서 bbox2를 로드 후 해당 값에 인수값을 적용하여 
		// bbox class 의 필드명중 user_info에 적용하게되는 형태
		this.user_info = nm;
		System.out.println(this.user_info);
		System.out.println(this.user_rpw);
	}
}